print('Enter two numbers : ')
a = int(input())
b = int(input())
print('Enter your operation : ')
c = input()
if c == '+':
    print('Addition is : ', a+b)
elif c == '-':
    print('Subtraction is : ', a-b)
elif c == '*':
    print('Multiplication is : ', a*b)
elif c == '/':
    print('Division is : ', a/b)
